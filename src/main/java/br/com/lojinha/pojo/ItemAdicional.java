package br.com.lojinha.pojo;

public class ItemAdicional {
    private String nome;
    private int qtd;

    public String getNome() {
        return this.nome;
    }
    public void setNome(String novoNome) {
        this.nome = novoNome;
    }

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int novaQtd) {
        this.qtd = novaQtd;
    }
}

